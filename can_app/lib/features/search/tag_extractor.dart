/// 감정/상황 입력 문장 → 사전 정의 태그 목록으로 변환하는 유틸
///
/// MVP에서는 키워드 포함 여부로 간단하게 매칭합니다.
/// 이후 pgvector 기반 유사도 검색으로 교체 가능하도록 인터페이스를 분리합니다.
class TagExtractor {
  TagExtractor._();

  /// 태그 → 매칭 키워드 목록
  static const Map<String, List<String>> _tagKeywords = {
    // ── 감정 기반 태그 ──────────────────────────────────────────────────────
    '불안': ['불안', '걱정', '두렵', '무서', '긴장', '떨려', '초조', '겁'],
    '지침': ['지쳐', '지치', '힘들', '포기', '그만', '지겨', '피곤', '번아웃', '쉬고'],
    '면접': ['면접', '취업', '입사', '채용', '합격', '탈락', '이력'],
    '관계': ['관계', '친구', '갈등', '혼자', '외로', '소통'],
    // ── 카테고리 태그 (12개) ────────────────────────────────────────────────
    '철학': ['철학', '사색', '의미', '본질', '진리', '깨달', '사상'],
    '성공': ['성공', '목표', '비즈니스', '사업', '결과', '성취', '이루'],
    '독서': ['책', '독서', '읽', '글', '작가', '문학', '이야기', '소설'],
    '지혜': ['지혜', '지식', '배움', '연구', '발견', '탐구', '알고'],
    '창의력': ['창의', '예술', '그림', '음악', '디자인', '상상', '표현', '만들'],
    '도전': ['도전', '시작', '해보', '모험', '새로운', '용감', '할 수 있'],
    '용기': ['용기', '두려워도', '선택', '결단', '의지', '강해', '이겨'],
    '믿음': ['믿음', '신앙', '희망', '은혜', '신뢰', '종교'],
    '사랑': ['사랑', '연인', '가족', '그리움', '애정', '좋아', '설레'],
    '행복': ['행복', '기쁨', '즐거', '웃음', '미소', '긍정', '만족', '평화'],
    '자기계발': ['공부', '성장', '발전', '노력', '열심히', '배우', '향상', '습관', '자기'],
    '삶': ['삶', '인생', '살아', '하루', '일상', '시간', '순간', '살면'],
  };

  /// 입력 문장에서 매칭되는 태그 목록 반환
  static List<String> extract(String input) {
    if (input.trim().isEmpty) return [];
    final lower = input.toLowerCase();
    return _tagKeywords.entries
        .where((entry) => entry.value.any((kw) => lower.contains(kw)))
        .map((entry) => entry.key)
        .toList();
  }

  /// 입력이 모호할 때 최소한의 추천이 가능하도록 기본 태그를 제공
  static List<String> fallbackTags(String input) {
    final lower = input.toLowerCase();
    if (lower.contains('힘들') || lower.contains('지치')) {
      return const ['지침', '자기계발', '행복'];
    }
    if (lower.contains('불안') || lower.contains('걱정') || lower.contains('초조')) {
      return const ['불안', '용기', '믿음'];
    }
    if (lower.contains('외로') || lower.contains('사랑') || lower.contains('관계')) {
      return const ['관계', '사랑', '행복'];
    }
    return const ['삶', '자기계발', '행복'];
  }

  /// 전체 태그 목록 (검색 탭 칩 UI용)
  static List<String> get allTags => _tagKeywords.keys.toList();
}
